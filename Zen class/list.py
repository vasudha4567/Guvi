#append
num=["1","2","3"]
num.append("4")
print(num)

#clear
num=["1","2","3"]
num.clear()
print(num)

#copy
num=["1","2","3"]
num.copy()
print(num)

#count
num=["red","blue","green"]
num.count("green")
print(num)

#extend
list1=["red","blue","green"]
list2=["white","pink","orange"]
list1.extend(list2)
print(list1)

#index
list1=["red","blue","green"]
a=list1.index("green")
print(a)

#insert
fruits = ['apple', 'banana', 'cherry']
fruits.insert(1, "orange")
print(fruits)

#pop
fruits=['apple', 'banana', 'cherry']
fruits.pop(1)
print(fruits)

#remove
num=["red","blue","green"]
num.remove("red")
print(num)

#reverse
num=["red","blue","green"]
num.reverse()
print(num)

#sort
cars=["Ford","BMW","Volvo"]
cars.sort()
print(cars)


